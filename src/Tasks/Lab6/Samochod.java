package Tasks.Lab6;

public class Samochod {
    String Marka;
    String Model;
    String Nadwozie;
    String Kolor;
    int RokProdukcji;
    int Przebieg;

    public Samochod(String marka, String model, String nadwozie, String kolor, int rokProdukcji, int przebieg) {
        Marka = marka;
        Model = model;
        Nadwozie = nadwozie;
        Kolor = kolor;
        RokProdukcji = rokProdukcji;
        Przebieg = przebieg;
    }
}
